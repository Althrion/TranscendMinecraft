package com.althrion.mod.variabledump;

public class Variables {

	private int cp;
	private float damageReduction;
	private int damageAbsorb;
	
	public int getCP() {
		return this.cp;
	}
	
	public void setCP(int cp) {
		this.cp = cp;
	}
	
	public float getDMGRed() {
		return this.damageReduction;
	}
	
	public void setDMGRed(float damageReduction) {
		this.damageReduction = damageReduction;
	}
	
	public int getDMGAbsorb() {
		return this.damageAbsorb;
	}
	
	public void setDMGAbsorb(int damageAbsorb) {
		this.damageAbsorb = damageAbsorb;
	}
	
}
